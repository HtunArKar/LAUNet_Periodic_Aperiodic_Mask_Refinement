# import necessary packages
from torch.utils.data import Dataset
from natsort import natsorted
from torch.nn import functional as F
import numpy as np
import torchaudio
import torch
import os


class CustomDataset(Dataset):
    def __init__(self, clean_files, noisy_files, sampling_rate=16000, cut_len= 16000 * 2):
        super(CustomDataset, self).__init__()

        self.cut_len = cut_len
        self.sampling_rate = sampling_rate
        self.clean_files = clean_files
        self.noisy_files = noisy_files

    def __len__(self):
        return len(self.clean_files)

    def __getitem__(self, idx):

        clean_file = self.clean_files[idx]
        noisy_file = self.noisy_files[idx]

        # load the current audios and perform resampling
        clean_ds, clean_sr = torchaudio.load(clean_file)
        noisy_ds, noisy_sr = torchaudio.load(noisy_file)

        if clean_sr != self.sampling_rate:

            resampler = torchaudio.transforms.Resample(orig_freq=clean_sr, new_freq=self.sampling_rate)
            clean_ds = resampler(clean_ds)
            clean_sr = self.sampling_rate


        if noisy_sr != self.sampling_rate:

            resampler = torchaudio.transforms.Resample(orig_freq=noisy_sr, new_freq=self.sampling_rate)
            noisy_ds = resampler(noisy_ds)
            noisy_sr = self.sampling_rate

        assert clean_sr == noisy_sr, "All sampling rate must be the same."

        # remove batch dimension
        clean_ds = clean_ds.squeeze()
        noisy_ds = noisy_ds.squeeze()

        # check the length of clean and noisy audios
        length = len(clean_ds)

        assert length == len(noisy_ds), "Length of clean, noise and noisy audios must be the same."

        if length < self.cut_len:
            units = self.cut_len // length
            clean_ds_final = []
            noisy_ds_final = []

            for i in range(units):
                clean_ds_final.append(clean_ds)
                noisy_ds_final.append(noisy_ds)

            clean_ds_final.append(clean_ds[: self.cut_len % length])
            noisy_ds_final.append(noisy_ds[: self.cut_len % length])

            clean_ds = torch.cat(clean_ds_final, dim=-1)
            noisy_ds = torch.cat(noisy_ds_final, dim=-1)

        else:
            # randomly cut 2 seconds segment
            wav_start = np.random.randint(0, length - self.cut_len + 1)

            noisy_ds = noisy_ds[wav_start : wav_start + self.cut_len]
            clean_ds = clean_ds[wav_start : wav_start + self.cut_len]

        return clean_ds, noisy_ds, clean_file, length